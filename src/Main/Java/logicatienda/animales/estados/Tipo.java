package logicatienda.animales.estados;

/**
 * Enum que define los tipos de estado posibles de un animal.
 * Cada estado representa una condición emocional o física del animal.
 * Cada estado positivo tiene su contraparte negativa.
 */
public enum Tipo {
    FELIZ,
    TRISTE,
    SACIADO,
    HAMBRIENTO,
    SANO,
    ENFERMO,
    LIMPIO,
    SUCIO
}
